using ll = long long;

void solve() {
    int n;
    std::cin >> n;
    int ans = 0;
    while (n--) {
        int x;
        std::cin >> x;
        ans = std::max(ans, x);
    }
    std::cout << ans << '\n';
}

int main() {
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);

    int t;
    std::cin >> t;

    while (t--) {
        solve();
    }

    return 0;
}