void solve() {
    int a, b;
    std::cin >> a >> b;
    if (a % 2 == 0 && b % 2 == 0) {
        std::cout << "YES\n";
    } else if (a == b * 2) {
        std::cout << "YES\n";
    } else if (a % 2 == 1) {
        std::cout << "NO\n";
    } else if (b % 2 == 1 && (a && a % 2 == 0)) {
        std::cout << "YES\n";
    } else {
        std::cout << "NO\n";
    }
}

int main() {
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);

    int t;
    std::cin >> t;
    for (int i = 0; i < t; ++i) {
        solve();
    }
}
