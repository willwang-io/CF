typedef long long ll;

void solve() {
    int x;
    std::cin >> x;
    int cur = 0, y = 0;
    for (int i = 1; i < x; ++i) {
        int tmp = std::gcd(i, x) + i;
        if (tmp > cur) {
            y = i;
            cur = tmp;
        }
    }
    std::cout << y << '\n';
}

int main() {
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);
    int t;
    std::cin >> t;

    for (int i = 0; i < t; ++i) {
        solve();
    }
}
