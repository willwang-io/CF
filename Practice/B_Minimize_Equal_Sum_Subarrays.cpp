void solve() {
    int n;
    std::cin >> n;
    std::vector<int> a(n);
    for (auto &x: a) {
        std::cin >> x;
    }
    std::cout << a.back() << ' ';
    for (int i = 0; i < n - 1; ++i) {
        std::cout << a[i] << ' ';
    }
    std::cout << '\n';
}

int main() {
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);

    int t;
    std::cin >> t;
    for (int i = 0; i < t; ++i) {
        // std::cout << "Case #" << (i + 1) << ": ";
        solve();
    }
}
