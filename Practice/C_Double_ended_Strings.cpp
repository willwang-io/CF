void solve() {
    string a, b;
    cin >> a >> b;
    if (a == b) {
        cout << 0 << nl;
        return;
    }
    set<string> cnt;
    int longest = 0;
    for (int i = 0; i < a.size(); ++i) {
        for (int len = 1; len <= a.size() - i; ++len) {
            string tmp = a.substr(i, len);
            cnt.insert(tmp);
        }
    }
    for (int i = 0; i < b.size(); ++i) {
        for (int len = 1; len <= b.size() - i; ++len) {
            string tmp = b.substr(i, len);
            if (cnt.find(tmp) != cnt.end()) {
                umax(longest, (int) tmp.size());
            }
        }
    }
    cout << (a.size() - longest + b.size() - longest) << nl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    cin >> t;

    for (int i = 0; i < t; ++i) {
        solve();
    }
}