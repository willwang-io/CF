long long f(long long x) {
    if (x <= 1) {
        return 0;
    }
    return 64 - __builtin_clzll(x - 1);
}

void solve() {
    long long n, m, a, b;
    std::cin >> n >> m >> a >> b;
    long long dr = std::min(a, n - a + 1);
    long long dc = std::min(b, m - b + 1);
    std::cout << (1 + std::min(f(dr) + f(m), f(n) + f(dc))) << '\n';
}

int main() {
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);

    int t;
    std::cin >> t;
    for (int i = 0; i < t; ++i) {
        // std::cout << "Case #" << (i + 1) << ": ";
        solve();
    }
}
