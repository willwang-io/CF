void solve() {
    int n;
    std::cin >> n;
    std::vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        std::cin >> a[i];
    }
    std::sort(a.begin(), a.end());
    if (a.front() % 2 == a.back() % 2) {
        std::cout << "0\n";
        return;
    } 
    int i = 0;
    while (a[i] % 2 != a.back() % 2) {
        ++i;
    }
    int j = n - 1;
    while (a[j] % 2 != a.front() % 2) {
        --j;
    }
    std::cout << std::min(i, n - j - 1) << '\n';
}

int main() {
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);

    int t;
    std::cin >> t;
    for (int i = 0; i < t; ++i) {
        // std::cout << "Case #" << (i + 1) << ": ";
        solve();
    }
}
