void solve() {
    int n;
    std::cin >> n;
    std::vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        std::cin >> a[i];
    }
    bool ok = true;
    for (int i = 1; i < n; ++i) {
        int tmp = std::abs(a[i] - a[i - 1]);
        if (!(tmp == 7 || tmp == 5)) {
            ok = false;
        }
    }
    std::cout << (ok ? "YES\n" : "NO\n");
}

