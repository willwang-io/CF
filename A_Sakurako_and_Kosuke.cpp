void solve() {
    int n;
    std::cin >> n;
    int x = 0;
    for (int i = 1; i < 1000; ++i) {
        x += std::pow(-1, i) * (2 * i - 1);
        if (std::abs(x) > n) {
            std::cout << (i % 2 == 1 ? "Sakurako\n" : "Kosuke\n");
            break;
        }
    }
}

int main() {
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);

    int t;
    std::cin >> t;
    for (int i = 0; i < t; ++i) {
        solve();
    }
}
