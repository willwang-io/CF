void solve() {
    long long a, b, k;
    std::cin >> a >> b >> k;
    long long d = std::gcd(a, b);
    long long x = a / d, y = b / d;
    std::cout << (x <= k && y <= k ? 1 : 2) << '\n';
}

int main() {
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);

    int t;
    std::cin >> t;
    for (int i = 0; i < t; ++i) {
        solve();
    }
}
