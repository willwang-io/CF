using ll = long long;

void solve() {
    ll n, a, b;
    std::cin >> n >> a >> b;
    if (a > b) {
        std::cout << 1 << '\n';
        return;
    }
    std::cout << n / a + ((n % a) != 0) << '\n';
}

int main() {
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);
    int t;
    std::cin >> t;

    for (int i = 0; i < t; ++i) {
        solve();
    }
}